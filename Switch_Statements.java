/*
 * Switch statements act the same way as condition (if else)
 * statements
 * They are essentially for optional based input, like a menu
 * Break statements are needed to exit the switch statement.
 * 
 * Outline:
 * switch(expression)
 * }
 * case value1:
 *  Condition met
 *  break;
 * case value2:
 *  Condition met
 *  break;
 * }
 * 
 * The expression in switch must be of type integer, char, enum,
 * or String
 * A "default" case will catch any other possibility
 * A switch statement only executes once, whereas a while loop
 * can go on forever until a conditional is met.
 */